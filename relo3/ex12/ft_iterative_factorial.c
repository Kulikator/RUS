int		ft_iterative_factorial(int nb)
{
	int a;
	int otv;

	if (nb < 0 || nb > 12)
		return (0);
	a = 2;
	otv = 1;
	while (a <= nb)
	{
		otv *= a;
		++a;
	}
	return (otv);
}
