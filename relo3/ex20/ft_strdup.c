#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*str;
	int		i;

	str = malloc(sizeof(*src));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
